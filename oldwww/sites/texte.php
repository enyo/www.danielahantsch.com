	<h1>&#8222;Zwischen Kind und Kunst&#8221;, Factory Kunsthalle Krems (2003)</h1>
	
	
	<p>
		Daniela Hantsch meist grossformatige Bilder zeigen landschaftliche Momentaufnahmen, Moment deshalb, weil Figuren in Bewegung festgefroren sind.
		Wie überdimensionierte Aquarelle konfrontieren sie den Betrachter mit einer märchenartigen Naivität. Ein spartanisch wirkender Duktus und viel
		freistehender Bildgrund erhöhen die flüchtige Transparenz des Geschehens. Wie auf verwaschenen Polaroids von Familienausflügen oder verblichenen
		Fresken öffnen sich weite Landschaftsausblicke, ganz im Gegensatz zu ihren früheren Garagen- und Schwimmbeckenbildern,
		in denen durch Reduktion des realen Raumes und des Lichts beinahe abstrahierte geschlossene Innenräume entwickelt werden.
	</p>
	<p>
		Die Fragilität und Starrheit des Gefüges von Landschaft und Figuren vermitteln aber auch etwas mysteriös bedrohliches,
		als ob die idyllische Szenerie jede Sekunde zerbrechen und in eine Katastrophe umschlagen könnte.
	</p>
	
	<p class="source">
		Mag.Sophie Geretsegger, Kunsthistorikerin,
		Universität für angewandte Kunst<br />
		(Er&ouml;ffnungsrede)
	</p>
