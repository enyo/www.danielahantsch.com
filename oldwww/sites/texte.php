	<h1>&#8222;Junge Malerei: Weiblich&#8221;, Galerie Altn&ouml;der (2001)</h1>

	<p>
		daniela hantsch erzählt in ihren bildern aus dem alltag. sie malt, was
		sie in ihrer unmittelbaren umgebung, im familiären bannkreis, mit
		achtsamkeit wahrnimmt: atmosphärische szenarien, die weder exotisch
		noch spektakulär sind, sondern mit erfrischender natürlichkeit und
		ungezwungenheit facettenreiche einblicke vermitteln. souverän geleitet
		die künstlerin durch ihre gemalten schnappschüsse. der eindruck von
		vermittelter heiterkeit, harmonie und nicht zuletzt auch freude gesellt
		sich wie von ganz alleine dazu. mit einer vorliebe für zarte texturen
		und eigenwillige farbklänge zaubert daniela hantsch mit sparsamen,
		bewusst lasierendem strich umräume, in denen stimmige und unbeschwerte
		beziehungen gelebt werden. sie erzählt geschichten von vertrautheit und
		vertrauen, von unkomplizierten bindungen, die elementar und natürlich
		gelingen.
	</p>
	<p>
		aus diesem blickwinkel gestaltet sich der alltag überraschend
		vielfältig und auch viel versprechend. mit einem untrüglichen gefühl
		für subtile vorgänge lotet daniela hantsch all das aus, was sie bewegt,
		was sich um sie bewegt. mit neugierde spürt sie das naheliegende auf
		und lässt sie poesie all dort entdecken, wo andere vielleicht nur
		routine und kräftezehren vermuten. eigentlich könnte es so einfach
		sein, auch neben den vielfältigsten familiären verpflichtungen zur
		leichtigkeit des seins zu finden....
	</p>
	
	<p class="source">
		Anneliese Kaar, <a href="http://www.galerie-altnoeder.com/" target="_blank">Galerie Altn&ouml;der</a><br />
		(Katalogtext)
	</p>

